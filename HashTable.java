package collections;

public class HashTable {

	public static void main(String[] args) {
		
		
		Integer i = 100111 ;
		
		System.out.println(i.hashCode());
		
		Character c = 'a' ;
		
		System.out.println(c.hashCode());
		
		String s = "ab" ;
		
		System.out.println(s.hashCode());
	}
}
